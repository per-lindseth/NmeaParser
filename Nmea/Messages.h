/// <summary>
///   Find the length of a nmea sentence ending with '\\x0A'.
/// </summary>
/// \param msg [in] A pointer to the first character in the sentence.
/// \pre exist i >= 0, msg[i] == '\\x0A'
/// \return Number of characters up to and including the first '\\x0A'
/// \note If theprecondition is not met the behaviour is undefined
inline size_t msglen(std::string_view msg) noexcept
{
	const size_t endPos{ msg.find_first_of('\x0A') };
	return (endPos == std::string_view::npos) ? 0 : endPos;
}

